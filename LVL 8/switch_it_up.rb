def switch_it_up(number)
  if number == 0
    "Zero"
  elsif number == 1
    "One"
  elsif number == 2
    "Two"
  elsif number == 3
    "Three"
  elsif number == 4
    "Four"
  elsif number == 5
    "Five"
  elsif number == 6
    "Six"
  elsif number == 7
    "Seven"
  elsif number == 8
    "Eight"
  elsif number == 9
    "Nine"
  elsif number == 10
    "Ten"
  end
end